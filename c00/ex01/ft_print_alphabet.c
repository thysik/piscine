#include <unistd.h>

void	ft_putchar(char a)
{
	write(1, &a, 1);
}

void	ft_print_alphabet(void)

{
	char	a;

	a = 97;
	while (a <= 122)
	{
		ft_putchar(a);
		a++;
	}
}
